// Number
let num  = 10;
console.log(num);
console.log(typeof num);

// String
let dez  = "10";
console.log(dez);
console.log(typeof dez);

// Boolean
let bool_  = dez == num;
console.log(bool_);
console.log(typeof bool_);

// Undefined
let undefinedVar_ = undefined;
console.log(undefinedVar_);
console.log(typeof undefinedVar_);

// Null
let null_  = null;
console.log(null_);
console.log(typeof null_);
